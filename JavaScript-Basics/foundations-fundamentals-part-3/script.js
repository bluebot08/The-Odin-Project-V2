function add7(number) {
  return number + 7;
}

console.log(add7(3));

function multiply(num1, num2) {
  return num1 * num2;
}

console.log(multiply(8, 91));

function capitalize(word) {
  word.toLowerCase();
  return word.charAt(0).toUpperCase() + word.slice(1);
}

console.log(capitalize("steFan"));
